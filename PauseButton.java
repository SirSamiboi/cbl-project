/**
 * Button used to pause the game.
 */

public class PauseButton extends Button {
    /**
     * Constructor.
     */
    PauseButton(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
        this.width = 30;
        this.height = 30;
        this.text = "II";
        
    }
}
